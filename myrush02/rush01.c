void	ft_putchar(char c);

void	rush01(x, y)
{
	int c1 = 0;
	int c2 = x - 1;
	int c3 = (x * y) - x;
	int c4 = (x * y) - 1;
	int i = 0;
	
	while (i < x * y)
	{
		char c = ' ';
		if (i / x == 0 || i % x == 0 || i / x == (y - 1) || i % x == (x - 1))
		   c = '*';	
		if (i == c1 || (i == c4))
			c = '/';
		if ((i == c2 && i != 0)|| (i == c3 && i != 0))
			c = '\\';
		ft_putchar(c);
		if (i % x == x-1)
		{
		ft_putchar('\n');
		}
		
		i++;
	}
}


