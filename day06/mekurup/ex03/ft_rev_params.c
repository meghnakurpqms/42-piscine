void	ft_putchar(char c);

int		main(int argc, char **argv)
{
	int i;
	int j;

	if (argc > 0)
	{
		i = argc - 1;
		while (i >= 1)
		{
			j = 0;
			while (argv[i][j] != '\0')
			{
				ft_putchar(argv[i][j]);
				j++;
			}
			ft_putchar('\n');
			i--;
		}
	}
	return (0);
}
